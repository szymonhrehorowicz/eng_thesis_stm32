void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
    if (htim == &htim3)
    {
        if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
        {
            /*
             Calculate the measured speed of the FAN
             */
            static const float FAN_CLK_FREQ = 84000000.0f;
            static const float FAN_CLK_PRES = 42000.0f;
            uint32_t tim_val = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
            float fan_speed = FAN_CLK_FREQ / FAN_CLK_PRES / (tim_val + 1.0f) * 30.0;
            fanController.speed = (uint16_t)fan_speed;
        }
    }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim == &htim4)
    {
        HAL_GPIO_TogglePin(LED_POWER_GPIO_Port, LED_POWER_Pin);
        FanController_update(&fanController);
        CoilController_update(&coilController);
    }
}
